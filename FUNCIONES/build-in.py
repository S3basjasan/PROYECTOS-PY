# Funciones integradas por python
numeros = [1, 2, 3, 4, 5]

# Encuentra el numero mayor de una lista
print(max(numeros))

# Encuentra el numero menor de una lista
print(min(numeros))

# Para redondear con "," se muestra los decimales que queremos
num = 23.4912
print(round(num, 2))

# Retorna si es Falso -> 0,  vacio, False, ninguno 
# True -> distinto de 0, cadena
print(bool(None))
print(bool(12))

# Retorna True si los valores son verdaderos
print(all([12, 2, "a", 0]))

# Suma
print(sum(numeros))